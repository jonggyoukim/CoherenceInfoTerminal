package com.oracle.coherence.info.model;

import java.util.List;
import java.util.Map;
import java.util.SortedMap;

import javax.management.MBeanServerConnection;
import javax.management.openmbean.TabularData;

import com.oracle.coherence.info.helper.VisualVMModel;

/**
 * Defines the methods for retrieving JMX data from an MBean server.
 *
 * @author Tim Middleton
 */
public interface DataRetriever {
	/**
	 * Return the report to run to collect the data.
	 *
	 * @return the report to run to collect the data
	 */
	public String getReporterReport();

	/**
	 * Return a {@link List} of statistics using standard JMX queries
	 *
	 * @param server
	 *            the {@link MBeanServerConnection} to use to query
	 *
	 * @return a {@link List} of statistics collected
	 */
	public List<Map.Entry<Object, Data>> getJMXData(MBeanServerConnection server, VisualVMModel model);

	/**
	 * Return a {@link List} of statistics given a {@link TabularData} output
	 * generated by the reporter.
	 *
	 * @param reportData
	 *            {@link TabularData} output generated by the reporter
	 *
	 * @return a {@link List} of service statistics
	 */
	public SortedMap<Object, Data> getReporterData(TabularData reportData, VisualVMModel model);

	/**
	 * Process a given {@link Object} array retrieved from a line in the report
	 * and return a representation of this.<br>
	 * Note: This may be available in a future Coherence release.
	 *
	 *
	 * @param aoColumns
	 *            {@link Object} array of column values
	 * @param model
	 *            the {@link VisualVMModel} to use
	 *
	 * @return an instance of {@link Data}
	 */
	public Data processReporterData(Object[] aoColumns, VisualVMModel model);
}
